#!/bin/bash
# run.sh - Швидкий запуск сервера локального NotebookLM
set -e

# Перевірка наявності віртуального середовища
if [ ! -d ".venv" ]; then
    echo "Помилка: Віртуальне середовище .venv не знайдено."
    echo "Будь ласка, спочатку запустіть ./setup_env.sh для встановлення залежностей."
    exit 1
fi

echo "=== Запуск локального NotebookLM ==="
echo "Веб-панель буде доступна за адресою: http://localhost:8000"
echo "Для завершення роботи натисніть Ctrl+C"
echo "-----------------------------------"

# Запуск сервера
exec .venv/bin/python3 cli.py serve "$@"
